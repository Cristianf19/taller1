
function mouseOver(boton) {
    document.getElementById(boton).style.backgroundColor = "#22223B";
    document.getElementById(boton).style.color = "#F2E9E4";
}

function mouseOut(boton) {
    document.getElementById(boton).style.backgroundColor = "#4a4e69";
    document.getElementById(boton).style.color = "#F2E9E4";
}
